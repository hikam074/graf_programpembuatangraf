def rumus_1(n,d):
    print(f"GRAF BER-ORDE :{n}: | BER-DERAJAT KELUAR :{d}:")
    header = []
    for i in range(0,n):
        header.append(i)
    print("label titik ->", header, "↓")
    for j in range(0,d) : # sebagai i
        kotak = []
        for i in range(0,n):
            titik_keluar = (d*i) + (j % n)
            while titik_keluar >= n :
                titik_keluar -= n
            while titik_keluar < 0 :
                titik_keluar += n
            kotak.append(titik_keluar)
        print(f"i = {j}       ->", kotak, "↓")

